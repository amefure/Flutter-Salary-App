abstract class PublicPolicyConfig {

  // ==============================
  // 基本情報
  // ==============================

  static const String version = 'v1.0.0';
  static const String effectiveDate = '2026-02-21';
  static const String title = '給与情報公開機能 利用ポリシー';

  // ==============================
  // 公開対象データ（限定列挙）
  // ==============================

  static const List<String> publishedFields = [
    '総支給額',
    '手取り額',
    '支給年月',
    '支払い元名（任意設定時）',
    '職種（任意設定時）',
    '地域（任意設定時）',
  ];

  static const String publishScopeDescription = '''
本機能により公開される情報は、上記列挙項目に限定されます。
メールアドレス・生年月日・メモ欄等の個人識別情報は公開されません。
''';

  // ==============================
  // 個人特定リスク条項
  // ==============================

  static const String identificationRiskClause = '''
公開情報の組み合わせにより、第三者が特定の個人を推測する可能性があります。
利用者はそのリスクを理解し、自己の責任において公開を行うものとします。
''';

  // ==============================
  // 利用者責任条項
  // ==============================

  static const String userResponsibilityClause = '''
利用者は、公開情報の正確性について責任を負います。
虚偽情報の登録または第三者の権利を侵害する行為は禁止されます。
''';

  // ==============================
  // 禁止事項
  // ==============================

  static const String prohibitedActionsClause = '''
以下の行為を禁止します。
・虚偽情報の投稿
・他人への誹謗中傷
・スクレイピング等による大量取得
・営利目的での再利用
・法令違反行為
''';

  // ==============================
  // 第三者利用制限
  // ==============================

  static const String thirdPartyUseRestriction = '''
公開情報の無断転載・再配布・商用利用を禁止します。
''';

  // ==============================
  // データ保持・削除
  // ==============================

  static const String retentionPolicy = '''
公開データは、利用者が公開状態にある期間保持されます。
公開停止またはアカウント削除時は、合理的期間内に公開状態から削除されます。
''';

  static const String deletionRightClause = '''
利用者はいつでも公開を停止できます。
ただし、第三者が既に取得した情報について運営は管理できません。
''';

  // ==============================
  // 免責・損害賠償制限
  // ==============================

  static const String liabilityLimitationClause = '''
本機能に関連して生じた損害について、
運営の責任は、故意または重過失がある場合を除き、
直近12ヶ月間に当該利用者が支払った利用料金を上限とします。
''';

  // ==============================
  // サービス変更・終了
  // ==============================

  static const String serviceModificationClause = '''
運営は、本機能を予告なく変更または終了することがあります。
''';

  // ==============================
  // 改定・再同意
  // ==============================

  static const String revisionClause = '''
本ポリシーは改定される場合があります。
重要な変更がある場合、再同意を求めます。
''';

  // ==============================
  // 反社会的勢力排除
  // ==============================

  static const String antiSocialForcesClause = '''
利用者は反社会的勢力に該当しないことを表明保証します。
''';

  // ==============================
  // 準拠法・管轄
  // ==============================

  static const String governingLawClause = '''
本ポリシーは日本法に準拠します。
紛争が生じた場合、東京地方裁判所を第一審の専属的合意管轄とします。
''';

}
